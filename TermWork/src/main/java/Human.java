public class Human {
    private int age;
    private String name;
    private boolean isHuman;

    public Human(int age, String name, boolean isHuman) {
        this.age = age;
        this.name = name;
        this.isHuman = isHuman;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public boolean isHuman() {
        return isHuman;
    }
}
